package modelo;

public class Escudo {
	private String invictus;
	private int proteccion;
	
	public Escudo () {}

	public String getInvictus() {
		return invictus;
	}

	public void setInvictus(String invictus) {
		this.invictus = invictus;
	}

	public int getProteccion() {
		return proteccion;
	}

	public void setProteccion(int proteccion) {
		this.proteccion = proteccion;
	}
}
